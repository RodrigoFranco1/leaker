def buscar_en_dehashed(auth, search_field, query, page, size):
    base_url = "https://api.dehashed.com/search"
    search_query = f"{search_field}:{query}" if search_field != 'all' else query
    url = f"{base_url}?query={search_query}&page={page}&size={size}"
    headers = {'Accept': 'application/json'}

    try:
        response = requests.get(url, headers=headers, auth=auth)
        response.raise_for_status()
        return response.json()
    except requests.exceptions.HTTPError as err:
        response_text = err.response.text if err.response else "Sin respuesta"
        return f"Error de HTTP: {err}\nRespuesta: {response_text}"
    except Exception as err:
        return f"Error General: {err}"

def recorrer_todas_las_paginas(auth, search_field, query, size):
    print(f"Iniciando búsqueda para: {search_field}='{query}'")
    page = 1
    registro_numero = 1
    while True:
        print(f"Solicitando página {page}")
        resultado = buscar_en_dehashed(auth, search_field, query, page, size)

        if isinstance(resultado, str):
            print(resultado)
            break

        if not resultado.get('entries'):
            print("No se encontraron más resultados o saldo agotado.")
            break

        for entrada in resultado['entries']:
            print(f"Registro {registro_numero}:")
            for key, value in entrada.items():
                print(f"  {key.capitalize().replace('_', ' ')}: {value}")
            print("\n")
            registro_numero += 1

        page += 1
        time.sleep(0.2)

def procesar_lista(auth, search_field, file_path, size):
    with open(file_path, 'r') as file:
        for line in file:
            target = line.strip()
            if target:
                search_query = target if search_field == 'all' else f"{search_field}:{target}"
                print(f"Buscando {search_field} = {target}")
                recorrer_todas_las_paginas(auth, search_field, search_query, size)

